#include "libft_bonus.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*current;
	t_list	*next;

	current = *lst;
	while (current != NULL)
	{
		if(del != NULL)
			del((*current)->content);
		next = current->next;
		free(current);
	}
	*lst = NULL;
}
